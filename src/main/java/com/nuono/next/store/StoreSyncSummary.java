package com.nuono.next.store;

public class StoreSyncSummary {

    private Integer totalStores;

    private Integer connectedStores;

    private Integer pendingStores;

    private Integer totalSiteStores;

    private Integer connectedSiteStores;

    private Integer managerLinks;

    public Integer getTotalStores() {
        return totalStores;
    }

    public void setTotalStores(Integer totalStores) {
        this.totalStores = totalStores;
    }

    public Integer getConnectedStores() {
        return connectedStores;
    }

    public void setConnectedStores(Integer connectedStores) {
        this.connectedStores = connectedStores;
    }

    public Integer getPendingStores() {
        return pendingStores;
    }

    public void setPendingStores(Integer pendingStores) {
        this.pendingStores = pendingStores;
    }

    public Integer getTotalSiteStores() {
        return totalSiteStores;
    }

    public void setTotalSiteStores(Integer totalSiteStores) {
        this.totalSiteStores = totalSiteStores;
    }

    public Integer getConnectedSiteStores() {
        return connectedSiteStores;
    }

    public void setConnectedSiteStores(Integer connectedSiteStores) {
        this.connectedSiteStores = connectedSiteStores;
    }

    public Integer getManagerLinks() {
        return managerLinks;
    }

    public void setManagerLinks(Integer managerLinks) {
        this.managerLinks = managerLinks;
    }
}
