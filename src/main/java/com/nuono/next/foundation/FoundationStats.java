package com.nuono.next.foundation;

public class FoundationStats {

    private Integer roleCount;

    private Integer userCount;

    private Integer activeUserCount;

    private Integer storeLinkCount;

    private Integer authorizedStoreCount;

    private Integer noonBindingUserCount;

    public Integer getRoleCount() {
        return roleCount;
    }

    public void setRoleCount(Integer roleCount) {
        this.roleCount = roleCount;
    }

    public Integer getUserCount() {
        return userCount;
    }

    public void setUserCount(Integer userCount) {
        this.userCount = userCount;
    }

    public Integer getActiveUserCount() {
        return activeUserCount;
    }

    public void setActiveUserCount(Integer activeUserCount) {
        this.activeUserCount = activeUserCount;
    }

    public Integer getStoreLinkCount() {
        return storeLinkCount;
    }

    public void setStoreLinkCount(Integer storeLinkCount) {
        this.storeLinkCount = storeLinkCount;
    }

    public Integer getAuthorizedStoreCount() {
        return authorizedStoreCount;
    }

    public void setAuthorizedStoreCount(Integer authorizedStoreCount) {
        this.authorizedStoreCount = authorizedStoreCount;
    }

    public Integer getNoonBindingUserCount() {
        return noonBindingUserCount;
    }

    public void setNoonBindingUserCount(Integer noonBindingUserCount) {
        this.noonBindingUserCount = noonBindingUserCount;
    }
}
